//Comparaciones
const n = 23;
const n1 = "23";

console.log(n == n1);//true

//Comparador mas confiable
console.log(n === n1);//false, comparador estricto tambien compara el tipo de dato

//Diferente de
console.log(n != n1);//false
console.log(n !== n1);//True, comparador estricto tambien compara el tipo de dato